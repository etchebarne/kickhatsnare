use std::{
    collections::BTreeMap,
    env, fs,
    path::{Path, PathBuf},
    process::ExitCode,
};

use kickhatsnare_protocol::{ContractMethod, contract};
use serde_json::{Value, json};

fn main() -> ExitCode {
    match run() {
        Ok(()) => ExitCode::SUCCESS,
        Err(error) => {
            eprintln!("contract generation failed: {error}");
            ExitCode::FAILURE
        }
    }
}

fn run() -> Result<(), Box<dyn std::error::Error>> {
    let mut arguments = env::args().skip(1);
    let output = PathBuf::from(arguments.next().ok_or("missing output directory")?);
    let check = arguments.any(|argument| argument == "--check");
    let contract = contract();

    let typescript = render_typescript(contract.version, &contract.methods);
    let schema = render_schema(contract.version, &contract.methods)?;

    write_or_check(&output.join("ipc.ts"), &typescript, check)?;
    write_or_check(&output.join("ipc.schema.json"), &schema, check)?;
    Ok(())
}

fn render_typescript(version: u32, methods: &[ContractMethod]) -> String {
    let entries = methods
        .iter()
        .map(|method| {
            format!(
                "  \"{}\": {{\n    params: {};\n    result: {};\n  }};",
                method.name, method.params_type, method.result_type
            )
        })
        .collect::<Vec<_>>()
        .join("\n");

    format!(
        "// Generated by kickhatsnare-protocol. Do not edit.\n\n\
         export const PROTOCOL_VERSION = {version} as const;\n\n\
         export interface IpcContract {{\n{entries}\n}}\n\n\
         export type IpcMethod = keyof IpcContract;\n\
         export type ParamsFor<M extends IpcMethod> = IpcContract[M][\"params\"];\n\
         export type ResultFor<M extends IpcMethod> = IpcContract[M][\"result\"];\n"
    )
}

fn render_schema(version: u32, methods: &[ContractMethod]) -> Result<String, serde_json::Error> {
    let methods = methods
        .iter()
        .map(|method| {
            (
                method.name,
                json!({
                    "params": method.params_schema,
                    "result": method.result_schema,
                }),
            )
        })
        .collect::<BTreeMap<_, Value>>();

    serde_json::to_string_pretty(&json!({
        "protocolVersion": version,
        "methods": methods,
    }))
    .map(|json| format!("{json}\n"))
}

fn write_or_check(
    path: &Path,
    content: &str,
    check: bool,
) -> Result<(), Box<dyn std::error::Error>> {
    if check {
        if fs::read_to_string(path)? != content {
            return Err(format!("{} is stale; run generate:ipc", path.display()).into());
        }
        return Ok(());
    }

    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent)?;
    }
    fs::write(path, content)?;
    Ok(())
}
